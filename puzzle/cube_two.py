import numpy as np

def execute_move_full(state, move):
    new_state = {}
    new_state["co"] = move["cp"] @ state["co"]
    new_state["cp"] = move["cp"] @ state["cp"]
    if "co" in move:
        new_state["co"] = np.mod(new_state["co"]+move["co"], 3)
    return new_state

def execute_move_dr(state, move):
    return {"cp": move["cp"] @ state["cp"]}

